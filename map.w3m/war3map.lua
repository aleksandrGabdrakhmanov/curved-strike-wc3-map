                    color = player.color,
                    isSensitive = true
                },
                {
                    text = player.totalKills,
                    color = player.color,
                    isSensitive = false
                },
                {
                    text = player.totalDamage,
                    color = player.color,
                    isSensitive = false
                }
            })
